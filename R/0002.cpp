#include<bits/stdc++.h>
using namespace std;

int main()
{
   int X, Y;
   cin >> X >> Y;

   cout << X << " + " << Y << " = " << X + Y << endl;
   cout << X << " * " << Y << " = " << X * Y << endl;
   cout << X << " - " << Y << " = " << X - Y << endl;
   cout << X << " / " << Y << " = " << X / Y << endl;
  
   return 0;
}

/*  
Input
5 10

Output
5 + 10 = 15
5 * 10 = 50
5 - 10 = -5 
*/